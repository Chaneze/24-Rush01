int	ft_issudoku(char **argv)
{
	int i;
	int j;

	i = 1;
	while (argv[i])
	{
		j = 0;
		while (argv[i][j])
			j++;
		if (j != 9)
			return (0);
		i++;
	}
	if (i != 10)
		return (0);
	return (1);
}
