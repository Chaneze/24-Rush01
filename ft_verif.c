#include "ft_sudoku.h"

int	ft_verif(char **argv)
{
	int	i;
	int	j;
	int	k;

	i = 1;
	j = 0;
	k = 0;
	while (argv[i])
	{
		while (argv[i][j])
		{
			if (argv[i][j] >= '1' && argv[i][j] <= '9')
				k++;
			j++;
		}
		i++;
		j = 0;
	}
	return (k);
}
